package EpisodeVO.User;

public class PrevNextVO {
	
	private int epNum;
	private int prevEpNum;
	private int nextEpNum;
	
	public int getEpNum() {
		return epNum;
	}
	public void setEpNum(int epNum) {
		this.epNum = epNum;
	}
	public int getPrevEpNum() {
		return prevEpNum;
	}
	public void setPrevEpNum(int prevEpNum) {
		this.prevEpNum = prevEpNum;
	}
	public int getNextEpNum() {
		return nextEpNum;
	}
	public void setNextEpNum(int nextEpNum) {
		this.nextEpNum = nextEpNum;
	}
	
	

}
